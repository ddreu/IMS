            <strong>Start Matches:</strong>
            <p>
              Matches can only be started 30 minutes before or after the scheduled time. If this window is missed, the "Start Match" option will be inaccessible.
            </p>
          </li>
          <li>
            <strong>Point System:</strong>
            <p>
