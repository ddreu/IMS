          <li>
            <strong>Leaderboard and Player Stats:</strong>
            <ul>
              <li>View standings in the leaderboard for both overall points and wins for each game.</li>
              <li>To see player statistics, navigate to the teams page, view the roster, and select a specific player to view their stats.</li>
            </ul>
