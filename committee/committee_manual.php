          <li>
            <strong>Create a Bracket:</strong>
            <ul>
              <li>Select a grade level first for Basic Education (Basic Ed) teams.</li>
              <li>Ensure all teams have been added before generating the bracket.</li>
            </ul>
          </li>
          <li>
            <strong>Schedule Matches:</strong>
            <p>
              Once the bracket is created, schedule all matches. Matches that still have incomplete teams will not appear in the match list.
            </p>
          </li>
          <li>
            <strong>Check Game Scoring Rules:</strong>
            <p>
