        <h6>Welcome to the Intramurals Management System!</h6>
        <p>This manual provides a guide for school admins and committees to use the system effectively:</p>

        <h5>For School Admins</h5>
        <ol>
          <li>
            <strong>Add Available Games:</strong>
            <p>Start by adding all the games that will be available in the school.</p>
          </li>
          <li>
  <strong>Add Departments, Sections, Strands, and Courses:</strong>
  <p>
    Create the necessary departments and their respective sections, strands, or courses. 
    <br><em>Note:</em> Department admins are responsible for adding sections, strands, and courses for their department, but school admins also have the ability to do this. Once sections, strands, or courses are added, teams will be automatically created for all the games available in the school.
  </p>
</li>

          <li>
            <strong>Set Up the Pointing System:</strong>
            <p>
              Define the point system for 1st, 2nd, and 3rd places to be used throughout the intramurals.
            </p>
          </li>
          <li>
            <strong>Add Other Users:</strong>
            <p>
              Once games and departments are set up, proceed to add department admins and committees. 
              Ensure all games are added before assigning them to committees.
            </p>
          </li>
          <li>
            <strong>Manage Announcements:</strong>
            <p>
              School admins can create announcements that will display on the website's homepage.
            </p>
          </li>
          <li>
            <strong>Schedule Management:</strong>
            <p>
              School admins have full access to create, update, and cancel schedules for all departments. 
              Department admins can only manage schedules for their respective departments.
            </p>
          </li>
          <li>
            <strong>View Players and Statistics:</strong>
            <p>
              School admins can view player details and stats by navigating through departments. 
              Select a department to view the teams associated with its sections, strands, or courses. 
              From there, you can access the team rosters and view individual player details and stats.
            </p>
          </li>
          <li>
            <strong>View Leaderboards:</strong>
            <p>
              The leaderboards can be accessed to view standings:
              <ul>
                <li><strong>Overall Leaderboard:</strong> Displays cumulative points and wins across all games.</li>
                <li><strong>By Game:</strong> View standings specific to individual games.</li>
              </ul>
            </p>
          </li>
          <li>
            <strong>Additional Privileges:</strong>
            <ul>
              <li>Reset the leaderboard.</li>
              <li>Archive intramurals data.</li>
              <li>Create, update, or delete department admins or committees.</li>
            </ul>
          </li>
        </ol>
